import time # Biblioteca que Ajusta o Tempo de Execução do Código

NumeroIncrivelDeRepeticoes = 0 # Variável que Recebe o Loop

while NumeroIncrivelDeRepeticoes < 100: # Estrutura de Controle do Tipo Loop, no Caso é um Loop While
    time.sleep(0.5)  # Função do Módulo Padrão do Python "time" que Coloca um Delay na Execução
    print("Feliz Aniversário, meu bom!") # A Mensagem que Será Exibida X Vezes 

NumeroIncrivelDeRepeticoes += 1 # Acréscimo Para Fazer o Loop Rodar até o Limite Estabelecido
    